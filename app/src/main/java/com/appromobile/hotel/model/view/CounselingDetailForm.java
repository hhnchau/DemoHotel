public class CounselingDetailForm {
    private String content;
    private int counselingSn;
    private String lastUpdate;
    private String replyStaffName;
    private int replyStaffSn;
    private int sn;

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public int getCounselingSn() {
        return counselingSn;
    }

    public void setCounselingSn(int counselingSn) {
        this.counselingSn = counselingSn;
    }

    public String getLastUpdate() {
        return lastUpdate;
    }

    public void setLastUpdate(String lastUpdate) {
        this.lastUpdate = lastUpdate;
    }

    public String getReplyStaffName() {
        return replyStaffName;
    }

    public void setReplyStaffName(String replyStaffName) {
        this.replyStaffName = replyStaffName;
    }

    public int getReplyStaffSn() {
        return replyStaffSn;
    }

    public void setReplyStaffSn(int replyStaffSn) {
        this.replyStaffSn = replyStaffSn;
    }

    public int getSn() {
        return sn;
    }

    public void setSn(int sn) {
        this.sn = sn;
    }
}
