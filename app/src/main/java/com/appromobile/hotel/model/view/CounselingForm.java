public class CounselingForm {
    private String appUserNickName;
    private int appUserSn;
    private String createTime;
    private String lastUpdate;
    private int qaStatus;
    private int readStatus;
    private int scope;
    private int sn;
    private String title;

    public String getAppUserNickName() {
        return appUserNickName;
    }

    public void setAppUserNickName(String appUserNickName) {
        this.appUserNickName = appUserNickName;
    }

    public int getAppUserSn() {
        return appUserSn;
    }

    public void setAppUserSn(int appUserSn) {
        this.appUserSn = appUserSn;
    }

    public String getCreateTime() {
        return createTime;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }

    public String getLastUpdate() {
        return lastUpdate;
    }

    public void setLastUpdate(String lastUpdate) {
        this.lastUpdate = lastUpdate;
    }

    public int getQaStatus() {
        return qaStatus;
    }

    public void setQaStatus(int qaStatus) {
        this.qaStatus = qaStatus;
    }

    public int getReadStatus() {
        return readStatus;
    }

    public void setReadStatus(int readStatus) {
        this.readStatus = readStatus;
    }

    public int getScope() {
        return scope;
    }

    public void setScope(int scope) {
        this.scope = scope;
    }

    public int getSn() {
        return sn;
    }

    public void setSn(int sn) {
        this.sn = sn;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
